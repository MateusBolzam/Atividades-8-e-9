public class ConversaoTempo
{
    public static float minutosSegundos(float min) {  return min * 60;  }

    public static float horasMinutos(float horas) {  return horas * 60;  }

    public static float diasHoras(float dias) {  return dias * 24;  }

    public static float semanasDias(float semanas) {  return semanas * 7;  }

    public static float mesesDias(float mes) {  return mes * 30;  }

    public static float anosDias(float anos) {  return anos * 365.25f;  }
}
